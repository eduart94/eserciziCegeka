package esercizioStatico;

public class Calcolatore
{
	
	public static int somma(int x, int y)
	{
		
		int risultato = x+y;
		return risultato;
	}
	
	public static int moltiplicazione(int x, int y) 
	{
		int risultato = 0 ;
		risultato = x*y;
		return risultato;
	}
	public static int divisore(int x, int y) 
	{
		int risultato = 0;
		risultato = x/y;
		return risultato;
		
	}
}
